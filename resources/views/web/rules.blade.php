            <h3>What is a HOT lane?</h3>

            <p>The HOT lane isn’t a summer fad. HOT stands for High-Occupancy Toll. These are lanes or toll
                roads that have special access for high-occupancy vehicles, single-passenger vehicles like
                motorcycles, and other HOV lane autos. For example, a toll highway may have a better rate for
                HOVs.</p>

            <p>HOT lanes are different than Express Toll lanes, which don’t have special rates for
                high-occupancy vehicles.</p>


            <h3>Basic rules and manners for carpool lanes</h3>

            <p>Do NOT cross a solid line to enter or exit the carpool lane</p>

            <p>Do NOT block faster traffic to utilize the carpool lane</p>

            <p>Pull into a slower lane to allow faster traffic to pass when the line is not solid</p>

            <p>Pay attention to signs posting hours and number of occupants enforced</p>

            <p>The carpool lane is NOT a passing lane</p>


            <h3>What are normal carpool hours?</h3>

            <p>Different regions have carpool lanes with full-time or part-time hours of operation. They are
                guided by peak traffic hours and congestion patterns.</p>

            <h4>Part-time carpool lanes</h4>

            <p>For example, in Northern California, carpool lanes are only enforced Monday thru Friday during
                peak hours. Cities vary, but common hours may be 6AM-10AM and 3PM-7PM.</p>

            <h4>Full-time carpool lanes</h4>

            <p>Full-time carpool lanes run 24-hours, 7 days a week—and even on holidays. Los Angeles, renowned
                for terrible traffic, has full-time carpool lanes on many of its freeways.</p>

            <p>Dipping into the carpool lane “just for a minute” isn’t so easy in some areas. Carpool lanes,
                like many HOT lanes, are entirely separated roadways in some areas—almost like their own mini
                freeways.</p>

            <p>Who is allowed in the carpool lane?</p>

            <p>How many people can use the carpool lane varies regionally. Every region tries to adjust hours
                and requirements to the traffic in their specific areas.</p>

